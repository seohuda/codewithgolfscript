export interface StepInfo {
  group: string;
  order: number;
}

const STEP_MAP: Record<string, StepInfo> = (() => {
  const m: Record<string, StepInfo> = {};
  STEP_GROUPS.forEach((g) => {
    g.titles.forEach((t, i) => {
      m[t] = { group: g.name, order: i + 1 };
    });
  });
  return m;
})();

export function getStep(title: string): StepInfo {
  return STEP_MAP[title] ?? { group: "기타", order: 999 };
}

export default STEP_MAP;
